def quarantine_item(item: dict, reason: str) -> None:
    """Move item to quarantine — never allow re-publication."""
    QUARANTINE.mkdir(parents=True, exist_ok=True)
    item_id = item.get("id", hashlib.sha256(item.get("title","").encode()).hexdigest()[:16])
    qfile = QUARANTINE / f"{item_id}.json"
    payload = {
        "quarantined_at": utc_now(),
        "reason": reason,
        "original_item": item,
    }
    _atomic_write(qfile, payload)
    log.warning("[QUARANTINE] Item '%s' -> quarantine: %s", item.get("title","")[:50], reason)


