def run_enforcement(fix_mode: bool = False, report_only: bool = False) -> int:
    """
    Run all mandate checks. Returns exit code.
    0 = all pass, 1 = violations found, 2 = violations after fix attempt.
    """
    log.info("=" * 70)
    log.info("SENTINEL APEX MANDATE ENFORCER v170.0")
    log.info("Production Readiness Review Board — Authority: CTO/CISO/CTI Board")
    log.info("Mode: %s", "REPORT-ONLY" if report_only else ("FIX+ENFORCE" if fix_mode else "AUDIT"))
    log.info("=" * 70)

    # Load feed
    feed_path = REPO_ROOT / "api" / "feed.json"
    if not feed_path.exists():
        log.error("[ENFORCER] api/feed.json not found — cannot audit")
        return 1

    try:
        raw = json.loads(feed_path.read_text(encoding="utf-8"))
        items = raw if isinstance(raw, list) else []
    except Exception as e:
        log.error("[ENFORCER] Cannot parse feed: %s", e)
        return 1

    log.info("Loaded %d items from api/feed.json for audit", len(items))

    # ─────────────────────────────────────────────────────────────────────────
    # FIX MODE: Backfill provenance first
    # ─────────────────────────────────────────────────────────────────────────
    if fix_mode:
        log.info("[FIX] Backfilling provenance fields on all items...")
        items = [backfill_provenance(i) for i in items]
        # Write fixed feed
        try:
            tmp = feed_path.with_suffix(".tmp")
            tmp.write_text(json.dumps(items, indent=2, ensure_ascii=False), encoding="utf-8")
            os.replace(str(tmp), str(feed_path))
            log.info("[FIX] Provenance backfill written to api/feed.json")
        except Exception as e:
            log.error("[FIX] Cannot write: %s", e)

    # ─────────────────────────────────────────────────────────────────────────
    # RUN ALL MANDATE CHECKS
    # ─────────────────────────────────────────────────────────────────────────
    all_violations: list[MandateViolation] = []

    log.info("[M1]  Checking ZERO SYNTHETIC INTELLIGENCE...")
    v1 = check_mandate_1(items)
    all_violations.extend(v1)
    log.info("      Violations: %d", len(v1))

    log.info("[M3]  Checking SOURCE PROVENANCE ENFORCEMENT...")
    v3 = check_mandate_3(items)
    all_violations.extend(v3)
    log.info("      Violations: %d", len(v3))

    log.info("[M4]  Checking NO INTERNAL SELF-PUBLISHING...")
    v4 = check_mandate_4(items)
    all_violations.extend(v4)
    log.info("      Violations: %d", len(v4))

    log.info("[M6]  Checking CACHE CONTAMINATION...")
    v6 = check_mandate_6(REPO_ROOT)
    all_violations.extend(v6)
    log.info("      Violations: %d", len(v6))

    log.info("[M7]  Checking SOURCE DIVERSITY (>=90%% external)...")
    v7 = check_mandate_7(items)
    all_violations.extend(v7)
    log.info("      Violations: %d", len(v7))

    log.info("[M8]  Checking QUALITY SCORING (threshold=%.0f)...", QUALITY_THRESHOLD)
    v8 = check_mandate_8(items)
    all_violations.extend(v8)
    log.info("      Violations: %d", len(v8))

    log.info("[M9]  Checking STALE INTELLIGENCE (>%d days)...", STALE_DAYS)
    v9 = check_mandate_9(items)
    all_violations.extend(v9)
    log.info("      Violations: %d", len(v9))

    log.info("[M10] Checking ACTOR ATTRIBUTION GOVERNANCE...")
    v10 = check_mandate_10(items)
    all_violations.extend(v10)
    log.info("      Violations: %d", len(v10))

    log.info("[M11] Checking IOC VALIDATION...")
    v11 = check_mandate_11(items)
    all_violations.extend(v11)
    log.info("      Violations: %d", len(v11))

    # Quality scores for all items (for report)
    quality_scores = [(i.get("id","?"), i.get("title","")[:50], *compute_quality_score(i)) for i in items]
    avg_quality = sum(q[2] for q in quality_scores) / max(len(quality_scores), 1)

    # ─────────────────────────────────────────────────────────────────────────
    # COMPILE REPORT
    # ─────────────────────────────────────────────────────────────────────────
    mandate_counts: dict[int, int] = defaultdict(int)
    for v in all_violations:
        mandate_counts[v.mandate] += 1

    by_risk = defaultdict(list)
    for v in all_violations:
        by_risk[v.risk].append(v.to_dict())

    # Source diversity stats
    total = max(len(items), 1)
    internal_count = sum(
        1 for i in items
        if (i.get("source") or i.get("feed_source") or i.get("source_name") or "")
           in INTERNAL_SOURCES
    )
    external_count = total - internal_count

    report = {
        "generated_at": utc_now(),
        "enforcer_version": "170.0",
        "mode": "report_only" if report_only else ("fix_and_enforce" if fix_mode else "audit"),
        "total_items": len(items),
        "total_violations": len(all_violations),
        "violations_by_mandate": dict(mandate_counts),
        "violations_by_risk": {k: len(v) for k, v in by_risk.items()},
        "average_quality_score": round(avg_quality, 1),
        "source_diversity": {
            "total": total,
            "external": external_count,
            "internal": internal_count,
            "external_pct": round(external_count / total * 100, 1),
            "target_pct": EXTERNAL_SOURCE_MINIMUM * 100,
            "compliant": external_count / total >= EXTERNAL_SOURCE_MINIMUM,
        },
        "mandate_compliance": {
            f"mandate_{m}": mandate_counts.get(m, 0) == 0
            for m in [1, 3, 4, 6, 7, 8, 9, 10, 11]
        },
        "deployment_approved": len(all_violations) == 0,
        "critical_violations": by_risk.get("CRITICAL", []),
        "high_violations": by_risk.get("HIGH", [])[:20],  # cap for readability
        "quality_summary": {
            "avg_score": round(avg_quality, 1),
            "items_below_threshold": sum(1 for q in quality_scores if q[2] < QUALITY_THRESHOLD),
            "threshold": QUALITY_THRESHOLD,
        },
    }

    _atomic_write(REPORT_OUT, report)
    log.info("[ENFORCER] Report written: %s", REPORT_OUT)

    # ─────────────────────────────────────────────────────────────────────────
    # PRINT SUMMARY
    # ─────────────────────────────────────────────────────────────────────────
    log.info("=" * 70)
    log.info("MANDATE ENFORCEMENT SUMMARY")
    log.info("  Total items audited:    %d", len(items))
    log.info("  Total violations:       %d", len(all_violations))
    log.info("  CRITICAL violations:    %d", len(by_risk.get("CRITICAL", [])))
    log.info("  HIGH violations:        %d", len(by_risk.get("HIGH", [])))
    log.info("  MEDIUM violations:      %d", len(by_risk.get("MEDIUM", [])))
    log.info("  Average quality score:  %.1f / 100", avg_quality)
    log.info("  Source diversity:       %.1f%% external (target: %.0f%%)",
             external_count / total * 100, EXTERNAL_SOURCE_MINIMUM * 100)
    log.info("  Deployment approved:    %s", "YES" if not all_violations else "NO — BLOCKED")
    log.info("=" * 70)

    # M6 (stale archives) is informational — doesn't block CI deployment
    # Separate blocking violations from informational ones
    blocking_violations = [v for v in all_violations if v.mandate != 6]
    info_violations     = [v for v in all_violations if v.mandate == 6]

    if info_violations:
        log.warning("[M6] %d informational violations (stale archives — run --fix to archive)",
                    len(info_violations))

    if blocking_violations:
        blocking_mandates = {}
        for v in blocking_violations:
            blocking_mandates[v.mandate] = blocking_mandates.get(v.mandate, 0) + 1

        # P2-FIX (audit finding F2 / O1): a report_only run is GUARANTEED to
        # `return 0` a few lines below — that is this flag's documented purpose
        # (see --report help text: "do not block deployment"). Logging
        # "██ DEPLOYMENT BLOCKED ██" at ERROR severity on a path that cannot
        # block anything is self-contradicting: run #1551's own log showed
        # "██ DEPLOYMENT BLOCKED ██" / "274 blocking violations" immediately
        # followed by "[REPORT-ONLY] ... Exiting 0". That trains readers to
        # distrust ERROR-level output — including the times it's reporting a
        # real, actionable problem. Same counts, same data, same exit code:
        # only the framing changes, to match what the code actually does.
        if report_only:
            log.warning("[MANDATE ENFORCER] %d advisory violations across mandates "
                        "(report-only mode — NOT enforced, deployment proceeds)",
                        len(blocking_violations))
            for m, count in sorted(blocking_mandates.items()):
                log.warning("  MANDATE %2d: %d violations (advisory, non-blocking)", m, count)
            log.info("[REPORT-ONLY] Advisory report written to "
                     "data/health/mandate_enforcement_report.json. "
                     "Exiting 0 — this run does not gate deployment.")
            return 0

        log.error("[MANDATE ENFORCER] ██ DEPLOYMENT BLOCKED ██")
        log.error("[MANDATE ENFORCER] %d blocking violations across mandates",
                  len(blocking_violations))
        for m, count in sorted(blocking_mandates.items()):
            log.error("  MANDATE %2d: %d violations", m, count)

        return 2 if fix_mode else 1

    log.info("[MANDATE ENFORCER] ██ ALL BLOCKING MANDATES SATISFIED — DEPLOYMENT APPROVED ██")
    if info_violations:
        log.info("[MANDATE ENFORCER] %d informational M6 items need archival (non-blocking)",
                 len(info_violations))
    return 0


def main() -> int:
    parser = argparse.ArgumentParser(description="SENTINEL APEX Mandate Enforcer")
    parser.add_argument("--fix",    action="store_true", help="Auto-remediate fixable violations")
    parser.add_argument("--report", action="store_true", help="Report only — do not block deployment")
    args = parser.parse_args()
    return run_enforcement(fix_mode=args.fix, report_only=args.report)


if __name__ == "__main__":
    sys.exit(main())
