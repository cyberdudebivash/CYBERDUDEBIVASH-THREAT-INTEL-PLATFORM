def compute_quality_score(item: dict) -> tuple[float, dict]:
    """
    Score = sum of weighted signals (max 100).
    Signals:
      source_url present          20 pts
      source_name not internal    15 pts
      content_hash present        10 pts
      publication_timestamp       10 pts
      evidence_count >= 2         10 pts
      trust_score >= 7            10 pts
      attribution_confidence > 0  10 pts
      no synthetic patterns        5 pts
      ttps present                 5 pts
      ioc_count > 0                5 pts
    """
    score = 0.0
    breakdown = {}

    if item.get("source_url"):
        score += 20; breakdown["source_url"] = 20
    else:
        breakdown["source_url"] = 0

    src = item.get("source_name") or item.get("source") or item.get("feed_source") or ""
    if src and src not in INTERNAL_SOURCES:
        score += 15; breakdown["source_name_external"] = 15
    else:
        breakdown["source_name_external"] = 0

    if item.get("content_hash"):
        score += 10; breakdown["content_hash"] = 10
    else:
        breakdown["content_hash"] = 0

    if item.get("publication_timestamp"):
        score += 10; breakdown["publication_timestamp"] = 10
    else:
        breakdown["publication_timestamp"] = 0

    ev = item.get("evidence_count", 0)
    if isinstance(ev, (int, float)) and ev >= 2:
        score += 10; breakdown["evidence_count"] = 10
    else:
        breakdown["evidence_count"] = 0

    ts = item.get("trust_score", 0)
    if isinstance(ts, (int, float)) and ts >= 7:
        score += 10; breakdown["trust_score"] = 10
    else:
        breakdown["trust_score"] = 0

    conf = item.get("actor_confidence", 0) or item.get("attribution_confidence", 0)
    if isinstance(conf, (int, float)) and conf > 0:
        score += 10; breakdown["attribution_confidence"] = 10
    elif item.get("attribution_category"):
        score += 5; breakdown["attribution_confidence"] = 5
    else:
        breakdown["attribution_confidence"] = 0

    text = f"{item.get('title','')} {item.get('description','')}".lower()
    if not any(p.search(text) for p in SYNTHETIC_PATTERNS):
        score += 5; breakdown["no_synthetic_patterns"] = 5
    else:
        breakdown["no_synthetic_patterns"] = 0

    if item.get("ttps"):
        score += 5; breakdown["ttps_present"] = 5
    else:
        breakdown["ttps_present"] = 0

    ioc_count = item.get("ioc_count", 0) or item.get("indicator_count", 0)
    if isinstance(ioc_count, (int, float)) and ioc_count > 0:
        score += 5; breakdown["ioc_count"] = 5
    else:
        breakdown["ioc_count"] = 0

    return min(100.0, score), breakdown


