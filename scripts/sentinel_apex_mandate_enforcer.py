def _infer_source_from_url(url: str) -> tuple[str, float]:
    """Infer source name and trust score from source URL domain."""
    if not url:
        return "", 0.0
    try:
        domain = urlparse(url).netloc.lower().removeprefix("www.")
        for known_domain, name in SOURCE_NAME_MAP.items():
            if known_domain in domain:
                trust = 9.0 if any(d in domain for d in
                    ["cisa.gov", "nvd.nist.gov", "mandiant", "crowdstrike", "microsoft",
                     "paloaltonetworks", "securelist", "checkpoint"]) else 7.5
                return name, trust
        # Unknown domain but has URL — treat as external, moderate trust
        return domain, 6.0
    except Exception:
        return "", 0.0


def _infer_source_from_title(title: str) -> tuple[str, str, float]:
    """Infer source_name, source_url, trust_score from advisory title."""
    for pattern, src_name, src_url, trust in TITLE_SOURCE_MAP:
        if pattern.search(title):
            return src_name, src_url, trust
    return "", "", 0.0


def _compute_content_hash(item: dict) -> str:
    """SHA256 of title + source_url for dedup/provenance."""
    blob = f"{item.get('title','')}{item.get('source_url','')}{item.get('id','')}"
    return hashlib.sha256(blob.encode("utf-8")).hexdigest()


def _infer_publication_timestamp(item: dict) -> str:
    """Best-effort extraction of original publication timestamp."""
    for field in ("published_at", "published", "timestamp", "created_at",
                  "date", "pub_date", "generated_at"):
        val = item.get(field, "")
        if val and isinstance(val, str) and len(val) >= 10 and val[:4].isdigit():
            return val
    return ""


def backfill_provenance(item: dict) -> dict:
    """Auto-remediate provenance fields where possible."""
    updated = dict(item)

    # source_url — try to recover
    src_url = (item.get("source_url") or item.get("source") or
               item.get("blog_url") or "").strip()
    # If source is internal string, clear it as source_url
    if src_url in INTERNAL_SOURCES or not src_url.startswith("http"):
        src_url = ""

    # Infer from URL if available
    if src_url:
        src_name, trust = _infer_source_from_url(src_url)
    else:
        # Infer from title
        src_name, src_url, trust = _infer_source_from_title(item.get("title", ""))

    if src_url:
        updated["source_url"] = src_url
    if src_name:
        updated["source_name"] = src_name
    if trust > 0:
        updated["trust_score"] = round(trust, 1)

    # retrieval_timestamp — use generated_at or now
    if not item.get("retrieval_timestamp"):
        updated["retrieval_timestamp"] = item.get("generated_at", utc_now())

    # publication_timestamp — infer from item fields
    if not item.get("publication_timestamp"):
        pub_ts = _infer_publication_timestamp(item)
        if pub_ts:
            updated["publication_timestamp"] = pub_ts

    # content_hash
    if not item.get("content_hash"):
        updated["content_hash"] = _compute_content_hash(updated)

    # evidence_count — count actual evidence signals
    evidence = 0
    if updated.get("source_url"):  evidence += 1
    if updated.get("cve_ids"):     evidence += len(item.get("cve_ids", []))
    if updated.get("iocs"):        evidence += 1
    if updated.get("ttps"):        evidence += len(item.get("ttps", []) or [])
    if updated.get("cvss_score"):  evidence += 1
    updated["evidence_count"] = evidence

    # Fix source field — replace internal source string with real source name
    current_source = item.get("source", "") or item.get("feed_source", "")
    if current_source in INTERNAL_SOURCES and src_name:
        updated["source"]      = src_name
        updated["feed_source"] = src_name

    return updated


