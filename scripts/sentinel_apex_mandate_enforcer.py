def check_mandate_1(items: list[dict]) -> list[MandateViolation]:
    """MANDATE 1: Zero synthetic intelligence."""
    violations = []
    for item in items:
        text = f"{item.get('title','')} {item.get('description','')}"
        for pattern in SYNTHETIC_PATTERNS:
            if pattern.search(text):
                violations.append(MandateViolation(
                    mandate=1,
                    item_id=item.get("id", "?"),
                    title=item.get("title", ""),
                    description=f"Synthetic pattern detected: '{pattern.pattern}'",
                    risk="CRITICAL",
                    remediation="Quarantine item. Never publish synthetic intelligence.",
                    auto_fixable=False,
                ))
                break
    return violations


def check_mandate_3(items: list[dict]) -> list[MandateViolation]:
    """MANDATE 3: Source provenance enforcement."""
    violations = []
    for item in items:
        missing = [f for f in REQUIRED_PROVENANCE_FIELDS if not item.get(f)]
        if missing:
            violations.append(MandateViolation(
                mandate=3,
                item_id=item.get("id", "?"),
                title=item.get("title", ""),
                description=f"Missing provenance fields: {missing}",
                risk="HIGH",
                remediation="Run backfill_provenance() or reject item.",
                auto_fixable=True,
            ))
    return violations


def check_mandate_4(items: list[dict]) -> list[MandateViolation]:
    """MANDATE 4: No internal source self-publishing."""
    violations = []
    for item in items:
        src = (item.get("source") or item.get("feed_source") or
               item.get("source_name") or "").strip()
        if src in INTERNAL_SOURCES or src.upper() in {s.upper() for s in INTERNAL_SOURCES}:
            violations.append(MandateViolation(
                mandate=4,
                item_id=item.get("id", "?"),
                title=item.get("title", ""),
                description=f"Internal source '{src}' used as origin publisher",
                risk="CRITICAL",
                remediation="Replace with verified external source or reject.",
                auto_fixable=True,
            ))
    return violations


def check_mandate_6(repo_root: Path) -> list[MandateViolation]:
    """MANDATE 6: Cache contamination — stale/synthetic artifacts."""
    violations = []
    # Check for reports from old years
    reports_dir = repo_root / "reports"
    if reports_dir.exists():
        for year_dir in reports_dir.iterdir():
            if year_dir.is_dir() and year_dir.name.isdigit():
                year = int(year_dir.name)
                if year < datetime.now().year:
                    count = len(list(year_dir.rglob("*.html")))
                    if count > 0:
                        violations.append(MandateViolation(
                            mandate=6,
                            item_id=f"reports/{year_dir.name}/",
                            title=f"Stale report archive: {year_dir.name}",
                            description=f"{count} HTML reports from {year} in reports/ directory",
                            risk="HIGH",
                            remediation="Archive or delete stale year directories.",
                            auto_fixable=True,
                        ))
    # Check quarantine directory for re-publication attempts
    quarantine = repo_root / "data" / "quarantine"
    if quarantine.exists():
        qfiles = list(quarantine.glob("*.json"))
        if qfiles:
            violations.append(MandateViolation(
                mandate=6,
                item_id="data/quarantine/",
                title=f"{len(qfiles)} quarantined items exist",
                description="Quarantine contains items — verify they are not re-entering pipeline",
                risk="MEDIUM",
                remediation="Ensure quarantine items are never re-processed.",
                auto_fixable=False,
            ))
    return violations


def check_mandate_7(items: list[dict]) -> list[MandateViolation]:
    """MANDATE 7: Source diversity >= 90% external."""
    if not items:
        return []
    total = len(items)
    internal_count = sum(
        1 for i in items
        if (i.get("source") or i.get("feed_source") or i.get("source_name") or "")
           in INTERNAL_SOURCES
    )
    external_count = total - internal_count
    external_pct   = external_count / total

    violations = []
    if external_pct < EXTERNAL_SOURCE_MINIMUM:
        violations.append(MandateViolation(
            mandate=7,
            item_id="feed_aggregate",
            title="Source diversity violation",
            description=(
                f"External sources: {external_count}/{total} "
                f"({external_pct*100:.1f}%) — minimum required: "
                f"{EXTERNAL_SOURCE_MINIMUM*100:.0f}%"
            ),
            risk="CRITICAL",
            remediation="Ingest more external sources. Reject internal-only items.",
            auto_fixable=True,
        ))
    return violations


def check_mandate_8(items: list[dict]) -> list[MandateViolation]:
    """MANDATE 8: Quality threshold."""
    violations = []
    for item in items:
        score, _ = compute_quality_score(item)
        if score < QUALITY_THRESHOLD:
            violations.append(MandateViolation(
                mandate=8,
                item_id=item.get("id", "?"),
                title=item.get("title", ""),
                description=f"Quality score {score:.1f} < threshold {QUALITY_THRESHOLD}",
                risk="HIGH",
                remediation="Enrich provenance fields to raise quality score.",
                auto_fixable=True,
            ))
    return violations


def check_mandate_9(items: list[dict]) -> list[MandateViolation]:
    """MANDATE 9: Stale intelligence prevention (> STALE_DAYS old)."""
    violations = []
    cutoff = datetime.now(timezone.utc) - timedelta(days=STALE_DAYS)
    cutoff_str = cutoff.strftime("%Y-%m-%d")

    for item in items:
        ts = (item.get("publication_timestamp") or item.get("timestamp") or
              item.get("published_at") or item.get("generated_at") or "")
        if not ts:
            continue
        try:
            # Parse ISO timestamp
            ts_clean = ts[:19].replace("T", " ").replace("Z", "")
            dt = datetime.strptime(ts_clean, "%Y-%m-%d %H:%M:%S").replace(tzinfo=timezone.utc)
            if dt < cutoff:
                violations.append(MandateViolation(
                    mandate=9,
                    item_id=item.get("id", "?"),
                    title=item.get("title", ""),
                    description=f"Intelligence dated {ts[:10]} is > {STALE_DAYS} days old (cutoff: {cutoff_str})",
                    risk="MEDIUM",
                    remediation="Quarantine stale item. Refresh from live source or reject.",
                    auto_fixable=False,
                ))
        except (ValueError, TypeError):
            pass
    return violations


def check_mandate_10(items: list[dict]) -> list[MandateViolation]:
    """MANDATE 10: Actor attribution governance."""
    violations = []
    banned_actors = {
        "UNC-CDB", "UNCLASSIFIED", "Unknown State-Sponsored Actor",
        "UNKNOWN ACTOR", "Ransomware Ecosystem Actor",
    }
    for item in items:
        actor = item.get("actor_tag", "") or item.get("actor_display_name", "")
        if actor in banned_actors:
            violations.append(MandateViolation(
                mandate=10,
                item_id=item.get("id", "?"),
                title=item.get("title", ""),
                description=f"Invalid actor attribution: '{actor}'",
                risk="HIGH",
                remediation="Run apex_attribution_engine_v2.py. Use 'Unattributed' for truly unknown.",
                auto_fixable=False,
            ))
    return violations


def check_mandate_11(items: list[dict]) -> list[MandateViolation]:
    """MANDATE 11: IOC validation."""
    violations = []
    for item in items:
        iocs = item.get("iocs", []) or []
        if not iocs:
            continue
        if not isinstance(iocs, list):
            continue
        for ioc in iocs:
            ioc_str = str(ioc)
            for pattern in PLACEHOLDER_IOC_PATTERNS:
                if pattern.search(ioc_str):
                    violations.append(MandateViolation(
                        mandate=11,
                        item_id=item.get("id", "?"),
                        title=item.get("title", ""),
                        description=f"Placeholder/synthetic IOC detected: '{ioc_str[:50]}'",
                        risk="HIGH",
                        remediation="Remove placeholder IOCs. Only publish validated indicators.",
                        auto_fixable=False,
                    ))
                    break
    return violations


